package com.xworkz.inheritance;

public class Star {
    public Star() {
        System.out.println("no args const of Star");
    }

    public void shine() {
        System.out.println("Star shines in the night sky");
    }

    public void emitHeat() {
        System.out.println("Star emits heat and radiation");
    }

    public void undergoFusion() {
        System.out.println("Star performs nuclear fusion");
    }

    public void collapse() {
        System.out.println("Star may collapse at end of life");
    }

    public void createGravity() {
        System.out.println("Star generates gravitational pull");
    }
}
