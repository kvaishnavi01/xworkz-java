package com.xworkz.inheritance;

public class Player {
    public Player() {
        System.out.println("no args const of Player");
    }

    public void train() {
        System.out.println("Player is training");
    }

    public void playMatch() {
        System.out.println("Player is playing a match");
    }

    public void travel() {
        System.out.println("Player is traveling to venue");
    }

    public void rest() {
        System.out.println("Player is taking rest");
    }

    public void followDiet() {
        System.out.println("Player is following a strict diet");
    }
}
