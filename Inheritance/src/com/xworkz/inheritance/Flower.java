package com.xworkz.inheritance;

public class Flower {
    public Flower() {
        System.out.println("no args const of Flower");
    }

    public void bloom() {
        System.out.println("Flower is blooming");
    }

    public void smell() {
        System.out.println("Flower smells pleasant");
    }

    public void absorbSunlight() {
        System.out.println("Flower is absorbing sunlight");
    }

    public void attractBees() {
        System.out.println("Flower is attracting bees");
    }

    public void wilt() {
        System.out.println("Flower is wilting");
    }
}
