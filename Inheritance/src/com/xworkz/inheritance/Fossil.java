package com.xworkz.inheritance;

public class Fossil {
    public Fossil() {
        System.out.println("no args const of Fossil");
    }

    public void display() {
        System.out.println("Fossil is displayed in museum");
    }

    public void analyze() {
        System.out.println("Fossil is being analyzed by scientists");
    }

    public void date() {
        System.out.println("Fossil age is being determined");
    }

    public void preserve() {
        System.out.println("Fossil is being preserved carefully");
    }

    public void catalog() {
        System.out.println("Fossil is added to the digital catalog");
    }
}
