package com.xworkz.inheritance;

public class Juice {
    public Juice() {
        System.out.println("no args const of Juice");
    }

    public void pour() {
        System.out.println("Juice is poured into glass");
    }

    public void mix() {
        System.out.println("Juice is mixed well");
    }

    public void chill() {
        System.out.println("Juice is chilled in fridge");
    }

    public void addSugar() {
        System.out.println("Sugar is added to juice");
    }

    public void serve() {
        System.out.println("Juice is served");
    }
}
