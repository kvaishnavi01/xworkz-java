package com.xworkz.inheritance;

public class Switch {
    public Switch() {
        System.out.println("no args const of Switch");
    }

    public void turnOn() {
        System.out.println("Switch is turned on");
    }

    public void turnOff() {
        System.out.println("Switch is turned off");
    }

    public void toggle() {
        System.out.println("Switch is toggled");
    }

    public void checkStatus() {
        System.out.println("Checking switch status");
    }

    public void replace() {
        System.out.println("Switch is replaced");
    }
}
