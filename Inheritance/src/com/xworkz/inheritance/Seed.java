package com.xworkz.inheritance;

public class Seed {
    public Seed() {
        System.out.println("no args const of Seed");
    }

    public void grow() {
        System.out.println("Seed starts to grow");
    }

    public void absorbWater() {
        System.out.println("Seed absorbs water from soil");
    }

    public void germinate() {
        System.out.println("Seed begins germination");
    }

    public void photosynthesize() {
        System.out.println("Seedling starts photosynthesis");
    }

    public void breakShell() {
        System.out.println("Seed breaks its shell");
    }
}
