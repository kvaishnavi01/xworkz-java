package com.xworkz.inheritance;

public class Artist {
    public Artist() {
        System.out.println("no args const of Artist");
    }

    public void draw() {
        System.out.println("Artist is drawing");
    }

    public void paint() {
        System.out.println("Artist is painting");
    }

    public void sketch() {
        System.out.println("Artist is sketching");
    }

    public void mixColors() {
        System.out.println("Artist is mixing colors");
    }

    public void displayArt() {
        System.out.println("Artist is displaying artwork");
    }
}
