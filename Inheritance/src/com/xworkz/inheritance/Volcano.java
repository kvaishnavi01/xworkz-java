package com.xworkz.inheritance;

public class Volcano {
    public Volcano() {
        System.out.println("no args const of Volcano");
    }

    public void erupt() {
        System.out.println("Volcano is erupting");
    }

    public void emitSmoke() {
        System.out.println("Volcano is emitting smoke");
    }

    public void shakeGround() {
        System.out.println("Volcano is shaking the ground");
    }

    public void releaseGases() {
        System.out.println("Volcano is releasing gases");
    }

    public void formCrater() {
        System.out.println("Volcano is forming a crater");
    }
}
