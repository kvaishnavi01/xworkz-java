package com.xworkz.inheritance;

public class Ghost {
    public Ghost() {
        System.out.println("no args const of Ghost");
    }

    public void appear() {
        System.out.println("Ghost appears faintly");
    }

    public void haunt() {
        System.out.println("Ghost starts haunting");
    }

    public void makeSound() {
        System.out.println("Ghost makes eerie noise");
    }

    public void disappear() {
        System.out.println("Ghost vanishes slowly");
    }

    public void scare() {
        System.out.println("Ghost scares people");
    }
}