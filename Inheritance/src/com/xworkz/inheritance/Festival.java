package com.xworkz.inheritance;

public class Festival {
    public Festival() {
        System.out.println("no args const of Festival");
    }

    public void celebrate() {
        System.out.println("Festival is being celebrated");
    }

    public void gatherPeople() {
        System.out.println("People are gathering for the festival");
    }

    public void performRituals() {
        System.out.println("Rituals are being performed");
    }

    public void decorate() {
        System.out.println("Festival area is being decorated");
    }

    public void serveFood() {
        System.out.println("Festival food is being served");
    }
}
