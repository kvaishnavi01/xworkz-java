package com.xworkz.inheritance;

public class Spoon {
    public Spoon() {
        System.out.println("no args const of Spoon");
    }

    public void scoop() {
        System.out.println("Spoon is used to scoop food");
    }

    public void stir() {
        System.out.println("Spoon is used to stir");
    }

    public void wash() {
        System.out.println("Spoon is being washed");
    }

    public void place() {
        System.out.println("Spoon is placed on the table");
    }

    public void pick() {
        System.out.println("Spoon is picked from the drawer");
    }
}
