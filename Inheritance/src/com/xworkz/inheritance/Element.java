package com.xworkz.inheritance;

public class Element {
    public Element() {
        System.out.println("no args const of Element");
    }

    public void react() {
        System.out.println("Element can react with other substances");
    }

    public void existNaturally() {
        System.out.println("Element may exist in nature");
    }

    public void conductHeat() {
        System.out.println("Element may conduct heat");
    }

    public void state() {
        System.out.println("Element can be solid, liquid or gas");
    }

    public void atomicNumber() {
        System.out.println("Element has a unique atomic number");
    }
}
