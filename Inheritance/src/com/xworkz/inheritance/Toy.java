package com.xworkz.inheritance;

public class Toy {
    public Toy() {
        System.out.println("no args const of Toy");
    }

    public void play() {
        System.out.println("Playing with the toy");
    }

    public void stop() {
        System.out.println("Stopped using the toy");
    }

    public void store() {
        System.out.println("Toy is stored safely");
    }

    public void clean() {
        System.out.println("Cleaning the toy");
    }

    public void showDetails() {
        System.out.println("Showing toy details");
    }
}
