package com.xworkz.inheritance;

public class Ninja {
    public Ninja() {
        System.out.println("no args const of Ninja");
    }

    public void sneak() {
        System.out.println("Ninja moves silently");
    }

    public void throwShuriken() {
        System.out.println("Ninja throws shuriken");
    }

    public void disguise() {
        System.out.println("Ninja blends in");
    }

    public void assassinate() {
        System.out.println("Ninja eliminates target");
    }

    public void escape() {
        System.out.println("Ninja vanishes in smoke");
    }
}