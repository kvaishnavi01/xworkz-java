package com.xworkz.inheritance;

public class Paint {
    public Paint() {
        System.out.println("no args const of Paint");
    }

    public void openCan() {
        System.out.println("Paint can is opened");
    }

    public void mix() {
        System.out.println("Paint is mixed well");
    }

    public void apply() {
        System.out.println("Paint is being applied");
    }

    public void dry() {
        System.out.println("Paint is drying");
    }

    public void cleanBrush() {
        System.out.println("Brush is cleaned after painting");
    }
}
