package com.xworkz.inheritance;

public class Magic {
    public Magic() {
        System.out.println("no args const of Magic");
    }

    public void castSpell() {
        System.out.println("Casting a basic spell");
    }

    public void vanishObject() {
        System.out.println("Making object disappear");
    }

    public void levitate() {
        System.out.println("Levitating small objects");
    }

    public void predict() {
        System.out.println("Predicting simple future");
    }

    public void teleport() {
        System.out.println("Attempting teleportation");
    }
}