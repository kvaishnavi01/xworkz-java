package com.xworkz.inheritance;

public class Plant {
    public Plant() {
        System.out.println("no args const of Plant");
    }

    public void absorbWater() {
        System.out.println("Plant absorbs water through roots");
    }

    public void performPhotosynthesis() {
        System.out.println("Plant performs photosynthesis");
    }

    public void grow() {
        System.out.println("Plant grows steadily");
    }

    public void releaseOxygen() {
        System.out.println("Plant releases oxygen into the air");
    }

    public void respondToSunlight() {
        System.out.println("Plant responds to sunlight direction");
    }
}
