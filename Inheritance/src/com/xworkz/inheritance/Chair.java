package com.xworkz.inheritance;

public class Chair {
    public Chair() {
        System.out.println("no args const of Chair");
    }

    public void support() {
        System.out.println("Chair provides support");
    }

    public void holdWeight() {
        System.out.println("Chair holds weight");
    }

    public void fold() {
        System.out.println("Chair can be folded");
    }

    public void move() {
        System.out.println("Chair can be moved");
    }

    public void place() {
        System.out.println("Chair is placed near the table");
    }
}
