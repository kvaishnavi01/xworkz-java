package com.xworkz.inheritance;

public class Clothing {
    public Clothing() {
        System.out.println("no args const of Clothing");
    }

    public void wear() {
        System.out.println("Clothing is worn");
    }

    public void wash() {
        System.out.println("Clothing is being washed");
    }

    public void dry() {
        System.out.println("Clothing is drying");
    }

    public void fold() {
        System.out.println("Clothing is being folded");
    }

    public void pack() {
        System.out.println("Clothing is being packed for travel");
    }
}
