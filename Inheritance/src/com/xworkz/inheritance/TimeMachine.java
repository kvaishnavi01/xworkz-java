package com.xworkz.inheritance;

public class TimeMachine {
    public TimeMachine() {
        System.out.println("no args const of TimeMachine");
    }

    public void activate() {
        System.out.println("Time machine powered on");
    }

    public void setDestination() {
        System.out.println("Setting time coordinates");
    }

    public void travel() {
        System.out.println("Initiating time jump");
    }

    public void observe() {
        System.out.println("Viewing historical events");
    }

    public void returnHome() {
        System.out.println("Returning to present time");
    }
}