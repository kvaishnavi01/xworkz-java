package com.xworkz.inheritance;

public class MemoryBank {
    public MemoryBank() {
        System.out.println("no args const of MemoryBank");
    }

    public void storeData() {
        System.out.println("MemoryBank stores binary-encoded experiences");
    }

    public void retrieveData() {
        System.out.println("MemoryBank retrieves precise memory snapshots");
    }

    public void archiveOldData() {
        System.out.println("MemoryBank archives old, unused memories");
    }

    public void eraseData() {
        System.out.println("MemoryBank deletes specific memory blocks");
    }

    public void protectData() {
        System.out.println("MemoryBank encrypts memory to prevent corruption");
    }
}
