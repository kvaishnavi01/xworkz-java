package com.xworkz.inheritance;

public class Weather {
    public Weather() {
        System.out.println("no args const of Weather");
    }

    public void change() {
        System.out.println("Weather is changing");
    }

    public void measureTemperature() {
        System.out.println("Measuring the temperature");
    }

    public void checkHumidity() {
        System.out.println("Checking humidity levels");
    }

    public void forecast() {
        System.out.println("Forecasting the upcoming weather");
    }

    public void display() {
        System.out.println("Displaying current weather status");
    }
}
