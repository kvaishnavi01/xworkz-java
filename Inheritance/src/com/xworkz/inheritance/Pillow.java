package com.xworkz.inheritance;

public class Pillow {
    public Pillow() {
        System.out.println("no args const of Pillow");
    }

    public void supportHead() {
        System.out.println("Pillow supports the head");
    }

    public void giveComfort() {
        System.out.println("Pillow provides comfort");
    }

    public void adjustShape() {
        System.out.println("Pillow adjusts to shape");
    }

    public void absorbSweat() {
        System.out.println("Pillow absorbs sweat");
    }

    public void cover() {
        System.out.println("Pillow is covered with fabric");
    }
}
