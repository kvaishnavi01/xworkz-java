package com.xworkz.inheritance;

public class House {
    public House() {
        System.out.println("no args const of House");
    }

    public void build() {
        System.out.println("House is built with bricks and mortar");
    }

    public void occupy() {
        System.out.println("House is occupied by a family");
    }

    public void decorate() {
        System.out.println("House is decorated with modern furniture");
    }

    public void maintain() {
        System.out.println("House is maintained regularly");
    }

    public void clean() {
        System.out.println("House is cleaned every weekend");
    }
}
