package com.xworkz.inheritance;

public class Satellite {
    public Satellite() {
        System.out.println("no args const of Satellite");
    }

    public void orbit() {
        System.out.println("Satellite is orbiting the Earth");
    }

    public void transmitData() {
        System.out.println("Satellite is transmitting data");
    }

    public void receiveSignal() {
        System.out.println("Satellite is receiving signals");
    }

    public void adjustPosition() {
        System.out.println("Satellite is adjusting its position");
    }

    public void monitorSystems() {
        System.out.println("Satellite is monitoring onboard systems");
    }
}
