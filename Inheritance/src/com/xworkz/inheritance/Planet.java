package com.xworkz.inheritance;

public class Planet {
    public Planet() {
        System.out.println("no args const of Planet");
    }

    public void rotate() {
        System.out.println("Planet rotates on its axis");
    }

    public void revolve() {
        System.out.println("Planet revolves around a star");
    }

    public void supportLife() {
        System.out.println("Checking if planet supports life");
    }

    public void reflectLight() {
        System.out.println("Planet reflects light from star");
    }

    public void showGravity() {
        System.out.println("Planet has gravitational force");
    }
}
