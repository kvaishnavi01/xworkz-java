package com.xworkz.inheritance;

public class Wheel {
    public Wheel() {
        System.out.println("no args const of Wheel");
    }

    public void rotate() {
        System.out.println("Wheel is rotating");
    }

    public void stop() {
        System.out.println("Wheel has stopped");
    }

    public void checkPressure() {
        System.out.println("Wheel pressure is checked");
    }

    public void balance() {
        System.out.println("Wheel is balanced");
    }

    public void align() {
        System.out.println("Wheel is aligned");
    }
}
