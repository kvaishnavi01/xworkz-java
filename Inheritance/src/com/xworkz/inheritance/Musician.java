package com.xworkz.inheritance;

public class Musician {
    public Musician() {
        System.out.println("no args const of Musician");
    }

    public void playInstrument() {
        System.out.println("Musician is playing an instrument");
    }

    public void practice() {
        System.out.println("Musician is practicing");
    }

    public void perform() {
        System.out.println("Musician is performing on stage");
    }

    public void compose() {
        System.out.println("Musician is composing music");
    }

    public void tuneInstrument() {
        System.out.println("Musician is tuning the instrument");
    }
}
