package com.xworkz.inheritance;

public class Organism {
    public Organism() {
        System.out.println("no args const of Organism");
    }

    public void grow() {
        System.out.println("Organism grows over time");
    }

    public void reproduce() {
        System.out.println("Organism reproduces to continue its species");
    }

    public void respondToStimuli() {
        System.out.println("Organism responds to environmental stimuli");
    }

    public void consumeEnergy() {
        System.out.println("Organism consumes energy for survival");
    }

    public void adapt() {
        System.out.println("Organism adapts to its surroundings");
    }
}
