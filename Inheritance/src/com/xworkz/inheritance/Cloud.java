package com.xworkz.inheritance;

public class Cloud {
    public Cloud() {
        System.out.println("no args const of Cloud");
    }

    public void form() {
        System.out.println("Cloud is forming");
    }

    public void rain() {
        System.out.println("Cloud is raining");
    }

    public void drift() {
        System.out.println("Cloud is drifting");
    }

    public void changeShape() {
        System.out.println("Cloud is changing shape");
    }

    public void disappear() {
        System.out.println("Cloud is disappearing");
    }
}