package com.xworkz.inheritance;

public class Employee {
    public Employee() {
        System.out.println("no args const of Employee");
    }

    public void work() {
        System.out.println("Employee is working");
    }

    public void attendMeeting() {
        System.out.println("Employee is attending a meeting");
    }

    public void submitReport() {
        System.out.println("Employee is submitting a report");
    }

    public void takeBreak() {
        System.out.println("Employee is taking a break");
    }

    public void logout() {
        System.out.println("Employee is logging out");
    }
}
