package com.xworkz.inheritance;

public class Designer {
    public Designer() {
        System.out.println("no args const of Designer");
    }

    public void brainstorm() {
        System.out.println("Designer is brainstorming ideas");
    }

    public void sketch() {
        System.out.println("Designer is sketching concepts");
    }

    public void selectMaterial() {
        System.out.println("Designer is selecting materials");
    }

    public void collaborate() {
        System.out.println("Designer is collaborating with team");
    }

    public void finalizeDesign() {
        System.out.println("Designer is finalizing the design");
    }
}
