package com.xworkz.inheritance;

public class Viking {
    public Viking() {
        System.out.println("no args const of Viking");
    }

    public void raid() {
        System.out.println("Viking starts coastal raid");
    }

    public void sail() {
        System.out.println("Viking navigates longship");
    }

    public void battle() {
        System.out.println("Viking engages in combat");
    }

    public void feast() {
        System.out.println("Viking celebrates with mead");
    }

    public void worship() {
        System.out.println("Viking prays to Odin");
    }
}