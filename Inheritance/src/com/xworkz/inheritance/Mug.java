package com.xworkz.inheritance;

public class Mug {
    public Mug() {
        System.out.println("no args const of Mug");
    }

    public void fill() {
        System.out.println("Mug is being filled");
    }

    public void hold() {
        System.out.println("Mug is being held");
    }

    public void wash() {
        System.out.println("Mug is being washed");
    }

    public void empty() {
        System.out.println("Mug is emptied");
    }

    public void store() {
        System.out.println("Mug is stored on shelf");
    }
}
