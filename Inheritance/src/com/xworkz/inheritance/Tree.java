package com.xworkz.inheritance;

public class Tree {
    public Tree() {
        System.out.println("no args const of Tree");
    }

    public void grow() {
        System.out.println("Tree is growing");
    }

    public void photosynthesize() {
        System.out.println("Tree is photosynthesizing");
    }

    public void provideShade() {
        System.out.println("Tree provides shade");
    }

    public void absorbWater() {
        System.out.println("Tree is absorbing water");
    }

    public void releaseOxygen() {
        System.out.println("Tree releases oxygen");
    }
}
