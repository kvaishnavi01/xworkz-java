package com.xworkz.inheritance;

public class Board {
    public Board() {
        System.out.println("no args const of Board");
    }

    public void write() {
        System.out.println("Board is written using a marker");
    }

    public void clean() {
        System.out.println("Board is being cleaned");
    }

    public void hang() {
        System.out.println("Board is hanging on the wall");
    }

    public void shine() {
        System.out.println("Board is shining under the lights");
    }

    public void display() {
        System.out.println("Board displays important info");
    }
}
