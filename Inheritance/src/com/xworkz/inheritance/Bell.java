package com.xworkz.inheritance;

public class Bell {
    public Bell() {
        System.out.println("no args const of Bell");
    }

    public void ring() {
        System.out.println("Bell rings loudly");
    }

    public void hang() {
        System.out.println("Bell is hanging on a hook");
    }

    public void polish() {
        System.out.println("Bell is polished to shine");
    }

    public void makeSound() {
        System.out.println("Bell makes ding-dong sound");
    }

    public void stop() {
        System.out.println("Bell stops ringing");
    }
}
